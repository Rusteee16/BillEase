package com.example.gstcalculator.data;

public class Contact {
    String name, email, uname, pass;


    public void setName(String name)
    {
        this.name  = name;
    }
    public String getName()
    {
        return this.name;
    }
    public void setPass(String pass)
    {
        this.pass  = pass;
    }
    public String getPass()
    {
        return this.pass;
    }

}
